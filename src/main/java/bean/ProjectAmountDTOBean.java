package bean;

public class ProjectAmountDTOBean {
	private int totalTransportationAmount;
	private int totalReimbursementAmount;
	private int totalAllowanceAmount;
	private int totalTransportationDetailAmount;
	private int totalProjectAmount;

	// Getter Setter
	public int getTotalTransportationAmount() {
		return totalTransportationAmount;
	}

	public void setTotalTransportationAmount(int totalTransportationAmount) {
		this.totalTransportationAmount = totalTransportationAmount;
	}

	public int getTotalReimbursementAmount() {
		return totalReimbursementAmount;
	}

	public void setTotalReimbursementAmount(int totalReimbursementAmount) {
		this.totalReimbursementAmount = totalReimbursementAmount;
	}

	public int getTotalAllowanceAmount() {
		return totalAllowanceAmount;
	}

	public void setTotalAllowanceAmount(int totalAllowanceAmount) {
		this.totalAllowanceAmount = totalAllowanceAmount;
	}

	public int getTotalTransportationDetailAmount() {
		return totalTransportationDetailAmount;
	}

	public void setTotalTransportationDetailAmount(int totalTransportationDetailAmount) {
		this.totalTransportationDetailAmount = totalTransportationDetailAmount;
	}

	public int getTotalProjectAmount() {
		return totalProjectAmount;
	}

	public void setTotalProjectAmount(int totalProjectAmount) {
		this.totalProjectAmount = totalProjectAmount;
	}
}
